let fruits = ['apple', 'mango', 'kiwi', 'grapes'];
console.log(fruits);
console.log(fruits.length); // 4
console.log(fruits[0]); // apple
console.log(fruits[1]); // mango
console.log(fruits[2]); // kiwi
console.log(fruits.push("cat")); //adds new element to end
console.log(fruits.push("dog", 'hippo', 'elephant')); //adds new element to the end
console.log(fruits.pop()); //remove last element
console.log(fruits.shift()); //removes first element
console.log(fruits.unshift()); //add new element to beginning

